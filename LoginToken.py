class LoginToken:

    def __init__(self, id, name, role):
        self.__id=id
        self.__name=name
        self.__role=role

    @property
    def id(self):
        return print(self.__id)

    @property
    def name(self):
        return print(self.__name)

    @property
    def role(self):
        return print(self.__role)

